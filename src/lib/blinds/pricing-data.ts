// Helper: get MSRP table for a product+fabric combo
export function getMsrpTable(product: ProductName, fabric: string): number[][] | null {
  switch (product) {
    case 'Zebra': return MSRP_ZEBRA[fabric] ?? null;
    case 'SHANGRILA': return MSRP_SHANGRILA[fabric] ?? null;
    case 'Cordless Cellular': return MSRP_CORDLESS_CELLULAR[fabric] ?? null;
    case 'SkylightHoneycomb': return MSRP_SKYLIGHT_HONEYCOMB[fabric] ?? null;
    case 'Drapery': return MSRP_DRAPERY[fabric] ?? null;
    case 'Sheer': return MSRP_SHEER[fabric] ?? null;
    case 'Shutters': return MSRP_SHUTTERS[fabric] ?? null;
    case 'Roller': return null; // Roller uses ROLLER_FABRICS + MSRP_ROLLER
    default: return null;
  }
}

// Helper: list available fabrics for a product
export function getAvailableFabrics(product: ProductName): string[] {
  switch (product) {
    case 'Zebra': return Object.keys(MSRP_ZEBRA);
    case 'SHANGRILA': return Object.keys(MSRP_SHANGRILA);
    case 'Cordless Cellular': return Object.keys(MSRP_CORDLESS_CELLULAR);
    case 'SkylightHoneycomb': return Object.keys(MSRP_SKYLIGHT_HONEYCOMB);
    case 'Roller': return Object.keys(ROLLER_FABRICS);
    case 'Drapery': return Object.keys(MSRP_DRAPERY);
    case 'Sheer': return [...Object.keys(MSRP_SHEER), 'Door Screen Sheer'];
    case 'Shutters': return Object.keys(MSRP_SHUTTERS);
    default: return [];
  }
}

