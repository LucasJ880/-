/**
 * 专家角色 System Prompt 注册表
 *
 * 从 agency-agents-zh 精选角色提取核心 prompt，
 * 适配青砚 createCompletion 调用方式。
 * Orchestrator / Skill 可根据任务类型选择对应专家角色。
 */

export interface ExpertRole {
  id: string;
  name: string;
  domain: string;
  systemPrompt: string;
}

const EXPERT_ROLES: Record<string, ExpertRole> = {};

function registerRole(role: ExpertRole): void {
  EXPERT_ROLES[role.id] = role;
}

export function getExpertRole(id: string): ExpertRole | undefined {
  return EXPERT_ROLES[id];
}

export function getExpertSystemPrompt(id: string): string | undefined {
  return EXPERT_ROLES[id]?.systemPrompt;
}

export function listExpertRoles(): ExpertRole[] {
  return Object.values(EXPERT_ROLES);
}

